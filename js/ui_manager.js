    // -------------------------------------------------------------------------
    // Helper Function: setTextSpan
    // -------------------------------------------------------------------------
    // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï innerText ‡∏Ç‡∏≠‡∏á span ‡∏•‡∏π‡∏Å‡∏ï‡∏±‡∏ß‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡πÉ‡∏ô Element
