// ข้อมูลเพิ่มเติมสำหรับการใช้งาน
const techniqueCategories = {
    beginner: [1, 2, 3, 4, 5],        // ท่าสำหรับผู้เริ่มต้น
    intermediate: [6, 7, 8, 9],       // ท่าระดับกลาง
    advanced: [10, 11, 12]            // ท่าระดับสูง
};

const positionDifficulty = {
    sitting: 1,        // ง่ายที่สุด
    standing: 2,       // ปานกลาง
    "low-standing": 3  // ยากที่สุด
};

// ฟังก์ชันช่วยในการดึงข้อมูล
const TechniqueHelper = {
    // ดึงข้อมูลท่าตาม ID และ position
    getTechnique: function(id, position) {
        if (techniqueData[id] && techniqueData[id].positions[position]) {
            return {
                name: techniqueData[id].name,
                ...techniqueData[id].positions[position]
            };
        }
        return null;
    },
    
    // ดึงท่าตามระดับความยาก
    getTechniquesByLevel: function(level) {
        return techniqueCategories[level] || [];
    },
    
    // ตรวจสอบความยากของ position
    getPositionDifficulty: function(position) {
        return positionDifficulty[position] || 1;
    },
    
    // ดึงท่าที่แนะนำสำหรับผู้เริ่มต้น
    getRecommendedTechniques: function() {
        return techniqueCategories.beginner.map(id => ({
            id: id,
            name: techniqueData[id].name,
            recommendedPosition: 'sitting'
        }));
    },
    
    // ดึงข้อมูลทั้งหมดของท่าหนึ่ง
    getFullTechniqueData: function(id) {
        return techniqueData[id] || null;
    }
};

